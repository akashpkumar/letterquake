const fs = require('node:fs')
const path = require('node:path')
const wordlist = require('wordlist-js')

const MAX_LENGTH = 8
const MIN_LENGTH = 3

const words = [...new Set([...wordlist.englishAll, ...wordlist.americanAll])]
  .filter((word) => /^[a-z]+$/.test(word))
  .filter((word) => word.length >= MIN_LENGTH && word.length <= MAX_LENGTH)
  .map((word) => word.toUpperCase())

const output = `// Generated by scripts/generate-dictionary.cjs
// Source: wordlist-js englishAll + americanAll
// Filter: alphabetic words, length ${MIN_LENGTH}-${MAX_LENGTH}

export const COMMON_WORDS = ${JSON.stringify(words, null, 2)}

export const DICTIONARY_SET = new Set<string>(COMMON_WORDS)
`

fs.writeFileSync(
  path.join(process.cwd(), 'src/game/dictionary.ts'),
  output,
  'utf8',
)

console.log(`Wrote ${words.length} words to src/game/dictionary.ts`)
